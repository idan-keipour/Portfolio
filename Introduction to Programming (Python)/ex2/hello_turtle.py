    turtle.goto(0,-50)
    turtle.down()
    turtle.circle(50)
    turtle.up()
    turtle.left(90)
    turtle.forward(50)
    turtle.down()
    turtle.forward(50)
    turtle.up()
    turtle.right(180)
    turtle.forward(50)
    turtle.down()
    turtle.left(60)
    turtle.forward(50)
    turtle.backward(50)
    turtle.right(120)
    turtle.forward(50)
