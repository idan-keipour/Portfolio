#  The program Draw a regular polygon with n sides of length l
def draw_regular_polygon(n, l):
    for i in range (n):
        turtle.left(360/n)
        turtle.forward(l)
# The program gets two lists which stand for the x,y coordinates, and connect them to a poligon.
def connect_all_pairs(x, y):
    turtle.up()
    turtle.goto(x[0],y[0])
    turtle.down()
    for i in range (len(x)):
      for j in range (len(x)):
        turtle.goto(x[i],y[i])
        turtle.goto(x[j],y[j])
    turtle.done()
